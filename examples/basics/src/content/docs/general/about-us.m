# 👋 Chào mừng bạn đến với Mscrew

**Mscrew** (tên đầy đủ là **MingSengCrew**) là một studio công nghệ trẻ, tập trung vào việc phát triển các Bot Discord và ứng dụng hữu ích nhằm mang lại giá trị tốt nhất cho cộng đồng người dùng Việt Nam.

Được thành lập vào ngày **20/02/2026** bởi **Minh Sang**, chúng tôi luôn giữ vững tôn chỉ: *Sáng tạo không giới hạn - Phát triển vì cộng đồng.*

---

## 🛠️ Sứ mệnh và Giá trị cốt lõi

Chúng tôi tin rằng công nghệ nên được tiếp cận một cách công bằng. Do đó, các dự án của Mscrew luôn tuân thủ các nguyên tắc:

* **Dành cho người Việt:** Mọi sản phẩm đều được tối ưu hóa ngôn ngữ và văn hóa sử dụng của người Việt.
* **Hoàn toàn miễn phí:** Chúng tôi cung cấp các tính năng chất lượng mà không thu phí cơ bản.
* **Nói không với Pay-to-Win:** Tại các trò chơi hoặc hệ thống Economy của Mscrew, sự nỗ lực và trải nghiệm của bạn là quan trọng nhất, không có yếu tố nạp tiền để giành lợi thế.

